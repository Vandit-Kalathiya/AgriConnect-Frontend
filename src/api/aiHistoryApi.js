import api from "../config/axiosInstance";
import { API_CONFIG } from "../config/apiConfig";
import { getUsernameFromToken } from "../../helper";

const HISTORY_API = api.create({
  baseURL: API_CONFIG.AI_BASE,
  timeout: 15000,
  withCredentials: true,
});
const inFlightRequests = new Map();

const getUserPhoneHeader = () => {
  const userPhone = getUsernameFromToken();
  return {
    "X-User-Phone": userPhone || "",
  };
};

const runDeduped = (key, requestFactory) => {
  if (inFlightRequests.has(key)) {
    return inFlightRequests.get(key);
  }

  const promise = requestFactory().finally(() => {
    inFlightRequests.delete(key);
  });

  inFlightRequests.set(key, promise);
  return promise;
};

export const fetchKisanMitraHistory = async ({ page = 0, size = 20 } = {}) => {
  const userPhone = getUsernameFromToken() || "guest";
  const key = `chat:${userPhone}:${page}:${size}`;

  return runDeduped(key, async () => {
    const response = await HISTORY_API.get("/chat/history", {
      params: { page, size },
      headers: getUserPhoneHeader(),
      withCredentials: true,
    });
    return response.data;
  });
};

